export type Locale = keyof typeof translations;
export type TranslationKeys = keyof (typeof translations)['en']; // Assuming 'en' has all keys

// Helper function to get translations, could be expanded for nested keys
export const getTranslation = (locale: Locale, section: keyof (typeof translations)['en'], key: string) => {
  const lang = translations[locale] || translations.en; // Fallback to English
  const sectionTranslations = lang[section];
  if (sectionTranslations && typeof sectionTranslations === 'object' && key in sectionTranslations) {
    // key exists → return the translation string
    // eslint-disable-next-line @typescript-eslint/consistent-return
    return (sectionTranslations as any)[key];
  }
  return `Missing translation: ${String(section)}.${key}`;
};
